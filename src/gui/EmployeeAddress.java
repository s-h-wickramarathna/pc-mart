    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Select City ....", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Employee Address ....", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            int city = cityMap.get(String.valueOf(jComboBox1.getSelectedItem()));
            String address = jTextField2.getText();

            MYSQL.Iud("INSERT INTO `address`(`address`,`employee_email`,`city_id`) VALUES ('" + address + "','" + email + "','" + city + "') ");
            loadAddress();
            resetFields();
        }


    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            int selectedRow = jTable1.getSelectedRow();

            if (selectedRow != -1) {
                jComboBox1.setSelectedItem(String.valueOf(jTable1.getValueAt(selectedRow, 2)));
                jTextField2.setText(String.valueOf(jTable1.getValueAt(selectedRow, 1)));
                jTextField3.setText(String.valueOf(jTable1.getValueAt(selectedRow, 0)));

                jTable1.setEnabled(false);
                jLabel5.setVisible(true);
                jTextField3.setVisible(true);
                jButton4.setEnabled(false);
                jButton3.setEnabled(true);
                jButton2.setEnabled(true);

            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

