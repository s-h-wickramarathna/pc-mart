    private void loadcities() {
        Vector<String> vector = new Vector<>();
        vector.add("Select City");

        try {
            ResultSet resultSet = MYSQL.Search("SELECT * FROM `city`");

            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                cityMap.put(resultSet.getString("name"), resultSet.getInt("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadAddress() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            ResultSet resultSet = MYSQL.Search("SELECT * FROM `address` INNER JOIN `city` ON `city`.`id`=`address`.`city_id` WHERE `employee_email`='" + email + "' ");

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("address"));
                vector.add(resultSet.getString("city.name"));
                model.addRow(vector);
            }

        } catch (Exception e) {
        }
    }

